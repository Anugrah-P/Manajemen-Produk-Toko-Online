    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (!jCheckBox1.isSelected()) {
        JOptionPane.showMessageDialog(this, "Anda harus mencentang persetujuan terlebih dahulu.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "INSERT INTO transaksi (nama_pemesan, area, tipe_rumah, luas_tanah_tersedia, harga_total, lama_cicilan, cicilan_per_bulan) " +
                     "VALUES (?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);

        String tipe = jRadioButton1.isSelected() ? "T-36" :
                      (jRadioButton3.isSelected() ? "T-45" :
                      (jRadioButton2.isSelected() ? "T-54" : ""));

        // Hapus karakter pemisah ribuan dan parsing angka
        String hargaRaw = txtfHarga.getText().replace(".", "").replace(",", "").replace(" ", "");
        String cicilanRaw = txtfCicilan.getText().replace(".", "").replace(",", "").replace(" ", "");

        long harga = Long.parseLong(hargaRaw);
        long cicilan = Long.parseLong(cicilanRaw);
        int lamaCicilan = Integer.parseInt(txtfLamaCicilan.getText());

        pst.setString(1, txtfNamaPemesan.getText());
        pst.setString(2, cmbboxArea.getSelectedItem().toString());
        pst.setString(3, tipe);
        pst.setDouble(4, Double.parseDouble(txtfLuasTanahTersedia.getText()));
        pst.setLong(5, harga);
        pst.setInt(6, lamaCicilan);
        pst.setLong(7, cicilan);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke database.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

