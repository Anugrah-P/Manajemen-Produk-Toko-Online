    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        // TODO add your handling code here:
        try {
            double hargaTanah = Double.parseDouble(txtfHargaTanah.getText());
            double hargaBangunan = Double.parseDouble(txtfHargaBangunan.getText());
            double luasTanah = Double.parseDouble(txtfLuasTanahTersedia.getText());
            double dp = Double.parseDouble(txtfDp.getText());
            int lamaCicilan = Integer.parseInt(txtfLamaCicilan.getText());

            double harga = (luasTanah * hargaTanah) + hargaBangunan;
            double ppn = 0.1 * harga;
            double cicilan = (harga + ppn - dp) / lamaCicilan;

            txtfHarga.setText(String.format("%.2f", harga));
            txtfPpn.setText(String.format("%.2f", ppn));
            txtfCicilan.setText(String.format("%.2f", cicilan));

            String tipe = jRadioButton1.isSelected() ? "T-36" : (jRadioButton3.isSelected() ? "T-45" : (jRadioButton2.isSelected() ? "T-54" : ""));
            String pesan = "Apakah data yang dimasukkan sudah benar?\n\n"
                    + "Nama: " + txtfNamaPemesan.getText() + "\n"
                    + "Area: " + cmbboxArea.getSelectedItem().toString() + "\n"
                    + "Tipe Rumah: " + tipe + "\n"
                    + "Harga: Rp " + txtfHarga.getText() + "\n"
                    + "PPn (10%): Rp " + txtfPpn.getText() + "\n"
                    + "Cicilan/Bulan: Rp " + txtfCicilan.getText();

            int konfirmasi = JOptionPane.showConfirmDialog(this, pesan, "Konfirmasi Data", JOptionPane.YES_NO_OPTION);
            if (konfirmasi == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(this, "Data dikonfirmasi.");
            } else {
                JOptionPane.showMessageDialog(this, "Silakan periksa kembali data Anda.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan input. Pastikan semua data terisi dengan benar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnProsesActionPerformed

